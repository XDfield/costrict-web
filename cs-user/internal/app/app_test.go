func TestReadyz_FailingCheckerReturns503(t *testing.T) {
	// Simulate a real DB ping failing — readiness must reflect it.
	r := NewRouter(newCfg("tok"), Deps{ReadyChecker: stubReadyErr{err: errors.New("db down")}})
	req := httptest.NewRequest(http.MethodGet, "/readyz", nil)
	w := httptest.NewRecorder()
	r.ServeHTTP(w, req)

	if w.Code != http.StatusServiceUnavailable {
		t.Fatalf("status = %d, want 503", w.Code)
	}
	if !strings.Contains(w.Body.String(), "db down") {
		t.Errorf("body = %q, want substring 'db down'", w.Body.String())
	}
}

