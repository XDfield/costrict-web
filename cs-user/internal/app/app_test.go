func init() { gin.SetMode(gin.TestMode) }

// stubReadyErr always returns the given error from Ready().
type stubReadyErr struct{ err error }

func (s stubReadyErr) Ready() error { return s.err }

// newCfg builds the minimum valid config for tests.
func newCfg(token string) *config.Config {
	return &config.Config{
		HTTP:     config.HTTPConfig{Port: "8081", Mode: "test"},
		Internal: config.InternalConfig{Token: token},
	}
}

func TestHealthz_AlwaysOK(t *testing.T) {
	r := NewRouter(newCfg("tok"), Deps{})
	req := httptest.NewRequest(http.MethodGet, "/healthz", nil)
	w := httptest.NewRecorder()
	r.ServeHTTP(w, req)

	if w.Code != http.StatusOK {
		t.Fatalf("status = %d, want 200", w.Code)
	}
	var body map[string]string
	if err := json.Unmarshal(w.Body.Bytes(), &body); err != nil {
		t.Fatalf("body unmarshal: %v (body=%s)", err, w.Body.String())
	}
	if body["status"] != "ok" {
		t.Errorf("status field = %q, want ok", body["status"])
	}
}

